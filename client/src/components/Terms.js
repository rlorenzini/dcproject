import React,{Component} from 'react';



class Terms extends Component {
  render(){
    return(
      <div>
      <h1 className="body">Terminology</h1>
<ul>
  <li><b>Law Enforcement Officer:</b> Any person within a government agency granted authority to uphold the law, make arrests, and detain individuals during investigations.</li>
  <li><b>Jurisdiction:</b> The boundaries an officer has granted authority over: state; county; city; federal.</li>
  <li><b>Sworn:</b> The authority to uphold laws has been granted to a person within a specific jurisdiction.</li>
  <li><b>Deputized:</b> The authority to uphold all laws within all states of the United States has been granted to a person.</li>
  <li><b>Reasonable Suspicion:</b> Using the present facts, an <b>OFFICER</b> can articulate a potential crime,
    or that a person is armed and dangerous. Reasonable suspicion is needed to detain or pat down.</li>
  <li><b>Pat Down:</b> Also known as a "frisk". When an officer checks a person for weapons in a non-invasive manner.</li>
  <li><b>Search:</b> When an officer arrests a person, he checks the person for all personal items and possible contraband, weapons, or tools of the crime.</li>
  <li><b>Probable Cause:</b> Given the totality of the circumstances, any <b>REASONABLE</b> person would come to the same conclusion.
    Probable cause is necessary for an arrest, and is used by juries during court trials.</li>
  <li><b>Miranda Rights:</b> During an interrogation, people have individual rights, which <b>MUST</b> be told to them by the interrogating officer. </li>
  <li><b>Investigation:</b> When an officer is looking into whether or not a crime was committed, the alleged area and people involved are part of his investigation.
    Until the officer has removed all suspicion of a crime, the investigation continues and everyone possible involved in considered detained.</li>
  <li><b>Interrogation:</b> When an officer intends to question you about a specific crime. For an interrogation to be conducted, the officer must have probable cause
  and have taken you into custody (arrested you).</li>
    <li><b>Detained:</b> During an investigation, or when there is reasonable suspicion you are involved in a crime, you are <b>NOT</b> free to leave until
    the investigating officer has dispelled all suspicion.</li>
  <li><b>Use of Force:</b> The standard an officer is held to when gaining compliance from a person. </li>
  <li><b>Excessive Force:</b> When an officer uses one level of force for longer than is necessary, refuses to try another level of force, or exceeds an acceptable
  escalation of use of force.</li>
  <li><b>Suspect:</b> Any person possibly involved in a crime.</li>
  <li><b>Victim:</b> Any person, business, or the jurisdiction which is the target of a crime. The jurisdiction is the victim of traffic violations.</li>
  <li><b>Miranda Rights:</b> Your legal rights which must be read to you before an interrogation.</li>
</ul>
      </div>
    )
  }
}
export default Terms
